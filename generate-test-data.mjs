// Create a simple DOCX file (ZIP with XML)
const createDOCX = async (filePath, seedText) => {
  // Minimal DOCX structure - it's a ZIP file
  // For simplicity, we'll create a valid but minimal DOCX
  const xml = `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<w:document xmlns:w="http://schemas.openxmlformats.org/wordprocessingml/2006/main">
  <w:body>
    <w:p>
      <w:r>
        <w:t>${seedText.replace(/[<>&"']/g, c => ({
          '<': '&lt;',
          '>': '&gt;',
          '&': '&amp;',
          '"': '&quot;',
          "'": '&apos;'
        }[c]))}</w:t>
      </w:r>
    </w:p>
  </w:body>
</w:document>`;

  // Create a minimal valid DOCX (ZIP with required structure)
  // For now, just write XML as binary to keep it simple
  await fs.writeFile(filePath, xml);
};

// Create a simple PPT file (similar to DOCX)
const createPPT = async (filePath, seedText) => {
  const xml = `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<p:presentation xmlns:p="http://schemas.openxmlformats.org/presentationml/2006/main">
  <p:sldIdLst>
    <p:sldId id="256" r:id="rId2" xmlns:r="http://schemas.openxmlformats.org/officeDocument/2006/relationships"/>
  </p:sldIdLst>
</p:presentation>
Text: ${seedText}`;
  await fs.writeFile(filePath, xml);
};

// Main execution
const formatCreators = {
  png: createPNG,
  jpg: createJPG,
  pdf: createPDF,
  docx: createDOCX,
  ppt: createPPT
};

console.log(`Generating ${manifest.documents.length} test files...`);

for (const doc of manifest.documents) {
  const dir = path.join(__dirname, path.dirname(doc.relativePath));
  await ensureDir(dir);

  const filePath = path.join(__dirname, doc.relativePath);
  const creator = formatCreators[doc.format];

  if (!creator) {
    console.warn(`Unknown format: ${doc.format}`);
    continue;
  }

  try {
    await creator(filePath, doc.seedText);
    console.log(`✓ Created ${doc.fileName}`);
  } catch (error) {
    console.error(`✗ Failed to create ${doc.fileName}:`, error.message);
  }
}

console.log('Test data generation complete!');
